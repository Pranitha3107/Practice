package Literals;
public class non_premitive_literal {
    public static void main(String[] args) {
        // Primitive literals
        int x = 10;
        float y = 3.14f;
        char letter = 'A';
        boolean isValid = true;

        // Non-primitive literals
        String name = "Pranitha";  // String literal
        int[] numbers = new int[]{1, 2, 3};  // Array literal (via new)
        String empty = null;  // null literal

    }
}
